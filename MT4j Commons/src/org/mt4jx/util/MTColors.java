	public static void main(String[] args) {
		try {
			BufferedReader br = new BufferedReader(new InputStreamReader(new FileInputStream(new File("D:/colors.csv"))));
			String line = null;
			while( (line=br.readLine())!=null){
				StringTokenizer st = new StringTokenizer(line, ";", false);
				String name = st.nextToken().trim();
				String hex = st.nextToken().trim();
				hex = hex.replace("#", "");
				int r = Integer.parseInt(hex.substring(0,2),16);
				int g = Integer.parseInt(hex.substring(2,4),16);
				int b = Integer.parseInt(hex.substring(4,6),16);
				String rgbString = (r + "," + g + "," + b);
				System.out.println("public static final MTColor " +name.toUpperCase()+ " = new MTColor("+rgbString+");");
				System.out.println("{colors.put(\"" +name.toUpperCase() + "\", " + name.toUpperCase() + ");}");
			}
		} catch (FileNotFoundException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		}
	}
	private static int cnt=0;
	
	public static MTColor getNext(){
		String[] colorNames = colors.keySet().toArray(new String[colors.size()]);
		Arrays.sort(colorNames);
		if(cnt>=colorNames.length){
			cnt = 0;
		}
		return colors.get(colorNames[cnt++]);
	}
	public static MTColor get(String name){
		return colors.get(name);
	}
